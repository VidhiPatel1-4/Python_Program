
# write a python program that takes two number from the user and perform the following operation : addition, subtraction,division,multiplication,modulo

Num1 = int(input("Enter a number :"))
Num2 = int(input("Enter second number:"))
print("\n")

# Addition of 2 Number:
Add = Num1 + Num2
print("Addition of", + Num1, "and", + Num2, "=", + Add)

# Subtraction of 2 Number:
Sub = Num1 - Num2
print("Subtraction of", + Num1, "and", + Num2, "=", + Sub)

# Division of 2 Number:
Div = Num1 / Num2
print("Division of", + Num1, "and", + Num2, "=", + Div)

# Multiplication of 2 Number:
Mul = Num1 * Num2
print("Multiplication of", + Num1, "and", + Num2, "=", + Mul)

# Modulus of 2 Number:
Mod = Num1 % Num2
print("Modulus of", + Num1, "and", + Num2, "=", + Mod)










